function getCatchDificultyByLevel(level)
	if (level >= 20 and level < 50) then
		return PCATCH_DIFFICULTY_EASY
	elseif (level >= 50 and level < 100) then
		return PCATCH_DIFFICULTY_MEDIUM
	elseif (level >= 100 and level < 150) then
		return PCATCH_DIFFICULTY_HARD
	elseif (level >= 150 and level < 999) then
		return PCATCH_DIFFICULTY_VERY_HARD
	end
return PCATCH_DIFFICULTY_NOTHING
end

function getCatchDifficultyLevel(difficulty)
	if (difficulty == PCATCH_DIFFICULTY_EASY) then
		return 20
	elseif (difficulty == PCATCH_DIFFICULTY_MEDIUM) then
		return 50
	elseif (difficulty == PCATCH_DIFFICULTY_HARD) then
		return 100
	elseif (difficulty == PCATCH_DIFFICULTY_VERY_HARD) then
		return 150
	end
return 999
