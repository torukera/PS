function doFinishCatch(cid, id, failed)
	doCreatureSetStorage(cid, PCATCH_DONE_STORAGE, -1)
	doCreatureSetStorage(cid, PCATCH_STARTED, 0)
	doCreatureSetStorage(cid, PCATCH_FIRST_POKEMON_ON_LIST, 0)
	doCreatureSetStorage(cid, PCATCH_SECOND_POKEMON_ON_LIST, 0)
	addDoneCatch(cid, 1)
	
	if not failed then
		local items = pokemonCatchTable[id].items
		
        local expzinha = pokemonCatchTable[id].experience
		
		doPlayerAddExperience(cid, pokemonCatchTable[id].experience)
        doSendAnimatedText(getCreaturePosition(cid), "+"..expzinha.." EXP", COLOR_LIGHTGREEN)
		
        doSendMagicEffect(getCreaturePosition(cid), 28)
        
        for i = 1, #items do
				setItemUniqueOwner(doPlayerAddItem(cid, items[i][1], items[i][2]), cid)
            end
        end
    end

