function sendCatchLists(cid)
	local taskList = {}
	local str = ""
	
	for i = 1, #pokemonCatchTable do
		if (getPlayerLevel(cid) >= getCatchDifficultyLevel(pokemonCatchTable[i].difficulty)) and (getPlayerLevel(cid) < getCatchDifficultyLevel(pokemonCatchTable[i].difficulty + 1)) then
			table.insert(taskList, pokemonCatchTable[i].pokemon)
		end
	end
	
	for i = 1, #taskList do
		if i == 1 then
			str = str.."{"..taskList[i].."}"
		elseif i == #taskList then
			str = str.." and {"..taskList[i].."}"
		else
			str = str..", {"..taskList[i].."}"
		end
	end
return str
end

function canStartCatch(cid, id)
	local d = pokemonCatchTable[id].difficulty
	local p = d + 1
	
	if (getPlayerLevel(cid) >= getCatchDifficultyLevel(d)) and (getPlayerLevel(cid) < getCatchDifficultyLevel(p) + 1) then
		if (getCreatureStorage(cid, getCatchStorageById(id)) < 1) then
			if (getFirstCatchPokemonId(cid) == id) or (getSecondCatchPokemonId(cid) == id) then
				return true
			else
				return false
			end
		else
			return false
		end
	end
return false
end

