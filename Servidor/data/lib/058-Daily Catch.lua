function doGetDoneCatchs(cid)
	if (getCreatureStorage(cid, PCATCH_AMOUNT_STORAGE) >= 1) then
		catchAmount = getCreatureStorage(cid, PCATCH_AMOUNT_STORAGE) 
	else
		catchAmount = 0
	end
	
return catchAmount
end

function haveDoneCatchTask(cid)
	if (getCreatureStorage(cid, PCATCH_DONE_STORAGE) >= 1) then
		return true
	else
		return false
	end
return false
end

function doFinishCatchTask(cid)
	doCreatureSetStorage(cid, PCATCH_DONE_STORAGE, 1) 
end

function addDoneCatch(cid, amount)
	doCreatureSetStorage(cid, PCATCH_AMOUNT_STORAGE, doGetDoneCatchs(cid) + amount)
	
	if (doGetDoneCatchs(cid) >= 3) then
		doCreatureSetStorage(cid, PCATCH_DONE_COOLDOWN, os.time() + 24 * (60 * 60)) -- 24 Hours
		doCreatureSetStorage(cid, PCATCH_AMOUNT_STORAGE, 0)
	end
end

function getCatchCooldown(cid)
	return getCreatureStorage(cid, PCATCH_DONE_COOLDOWN) - os.time()
end

function generateDualCList(cid)
	local taskList = {}
	local finalList = {}
	
	for i = 1, #pokemonCatchTable do
		if (getPlayerLevel(cid) >= getCatchDifficultyLevel(pokemonCatchTable[i].difficulty)) and (getPlayerLevel(cid) < getCatchDifficultyLevel(pokemonCatchTable[i].difficulty + 1)) then
			table.insert(taskList, pokemonCatchTable[i].pokemon)
		end
	end
	
	if (#taskList < 1) then
		return false
	end
	
	for i = 0, #taskList do
		local mR = math.random(#taskList)
		if not isInArray(finalList, taskList[mR]) and (#finalList < 2) then
			table.insert(finalList, taskList[mR])
		end
	end

	doCreatureSetStorage(cid, PCATCH_FIRST_POKEMON_ON_LIST, getCatchIdByName(finalList[1]))
	doCreatureSetStorage(cid, PCATCH_SECOND_POKEMON_ON_LIST, getCatchIdByName(finalList[2]))
return true
end

function hasDualCList(cid)
	if (getCreatureStorage(cid, PCATCH_FIRST_POKEMON_ON_LIST) >= 1) then
		return true
	end
return false
end

