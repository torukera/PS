pokemonNamesWithoutShiny = table_concat(NORMAL_1ST, NORMAL_2ND, NORMAL_3RD)

local NORMAL_1ST_SETTED = table.set(NORMAL_1ST)
local NORMAL_2ND_SETTED = table.set(NORMAL_2ND)
local NORMAL_3RD_SETTED = table.set(NORMAL_3RD)

function getPokemonGenerationByName(name)
    if (NORMAL_1ST_SETTED[name]) then
        return 1
    elseif (NORMAL_2ND_SETTED[name]) then
        return 2
    elseif (NORMAL_3RD_SETTED[name]) then
        return 3
    end
    return nil
end

function isPokemonName(name, exact)
    if (not exact) then
        string.lower(name)
        name = string.capitalise(name) -- name = name:gsub("^%l", string.upper)
    end
    return isInArray(pokemonsNames, name)
end

