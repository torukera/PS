function doPlayerAddTownMarks(cid, townName)
    if (not GUIDES[townName]) then
        log(LOG_TYPES.ERROR, "doPlayerAddTownMarks - Unknown town name.", getCreatureName(cid), townName)
        return
    end

    for _, mark in pairs(GUIDES[townName]) do
        doPlayerAddMapMark(cid, mark.position, mark.type, __L(cid, mark.description))
    end
end