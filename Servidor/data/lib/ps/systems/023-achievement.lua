function getPlayerAchievement(cid, achievId)
    local r = db.getResult(string.concat("SELECT 1 FROM `player_achievements` PA WHERE `PA`.`player_id`='",
            getPlayerGUID(cid), "' AND `PA`.`key`='", achievId, "' LIMIT 1;"))
	return r:getID() ~= -1
end

function doPlayerAchievementCheck(cid, achievId, var)
    if (getPlayerAchievement(cid, achievId)) then
        if (ACHIEVEMENT_SERIES[achievId]) then
            return doPlayerAchievementCheck(cid, ACHIEVEMENT_SERIES[achievId], var)
        end

    elseif (not ACHIEVEMENT_CHECKS[achievId] or ACHIEVEMENT_CHECKS[achievId](cid, var)) then
        local query = string.concat("INSERT INTO `player_achievements` (`player_id`, `key`) VALUES('",
            getPlayerGUID(cid), "', '", achievId, "');")
        if (db.executeQuery(query)) then
            doPlayerSendTextMessage(cid, MESSAGE_EVENT_ADVANCE, string.format(__L(cid, "You earned a new achievement: '%s'"), __L(cid, ACHIEVEMENT_NAMES[achievId])))
            doSendMagicEffect(getCreaturePosition(cid), EFFECT_FIREWORK_RED)
            doPlayerAddHighscoreValue(cid, HIGHSCORE_IDS.ACHIEVEMENTS,
                SCORE_BY_RANKS[ACHIEVEMENT_RANKS[achievId]])
            doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, string.format(__L(cid, "You received %s achievement score points."), SCORE_BY_RANKS[ACHIEVEMENT_RANKS[achievId]]))
            doPlayerAddStatistic(cid, PLAYER_STATISTIC_IDS.EARN_ACHIEVEMENT, 1)
            return true
        else
            log(LOG_TYPES.ERROR, "doPlayerAchievementCheck - Can't query.", getCreatureName(cid),
                achievId, var, query)
        end
    end
    return false
end

function getAchievementName(cid, achievId)
    if (getPlayerAchievement(cid, achievId)) then
        return __L(cid, ACHIEVEMENT_NAMES[achievId]) .. __L(cid, " (complete)")
    end
    return __L(cid, ACHIEVEMENT_NAMES[achievId])
end

function getAchievementDescription(achievId)
    return ACHIEVEMENT_DESCRIPTIONS[achievId]
end

function getAchievementSecret(achievId)
    return SECRET_ACHIEVEMENTS[achievId]
