local function getHighscore(highscoreId)
    --local dbResult, r = db.getResult("SELECT `player_storage`.`value`, `players`.`name` FROM `player_storage`, `players` WHERE `player_storage`.`key` = '".. HIGHSCORE_STORAGES[highscoreId] .."' AND `player_storage`.`player_id` = `players`.`id` AND `players`.`world_id` =".. getConfigValue("worldId") .." ORDER BY CAST(`player_storage`.`value` as SIGNED) DESC LIMIT ".. getConfigValue("highscoreDisplayPlayers") ..";"), {}
    local dbResult, r = db.getResult("SELECT `PS`.`value`, `P`.`name` FROM `player_storage` `PS` INNER JOIN `players` `P` ON `P`.`id` = `PS`.`player_id` WHERE `PS`.`key` = '" .. HIGHSCORE_STORAGES[highscoreId] .. "' AND `P`.`world_id` =" .. getConfigValue("worldId") .. " ORDER BY CAST(`PS`.`value` as SIGNED) DESC LIMIT " .. getConfigValue("highscoreDisplayPlayers") .. ";"), {}
    if dbResult:getID() == -1 then
        return r
    end
    repeat
        table.insert(r, dbResult:getDataString("name"))
        table.insert(r, dbResult:getDataInt("value"))
    until not dbResult:next()
    dbResult:free()
    return r
end

function doCaughtHighscoreCheck()
    if (type(getConfigValue("updateHighscores")) == "boolean" and not getConfigValue("updateHighscores")) then
        log(LOG_TYPES.WARNING, "doHighscoreCheck - updateHighscores is off.")
        return
    end

    local dateNow = os.date("%a %b %d %X %Y", os.time())
    for i, highscoreId in ipairs(HIGHSCORE_IDS) do
        local highscoreTable, text, k = getHighscore(highscoreId), {}, 1
        text[1] = "Highscore for "
        text[2] = HIGHSCORE_NAMES[highscoreId]
        text[3] = "\n\nRank Level - Player Name"
        for j = 1, #highscoreTable, 2 do
            text[#text + 1] = "\n"
            text[#text + 1] = k
            text[#text + 1] = ". "
            text[#text + 1] = highscoreTable[j]
            text[#text + 1] = " - "
            text[#text + 1] = highscoreTable[j + 1]
            k = k + 1
        end
        text[#text + 1] = "\n\nLast updated on:\n"
        text[#text + 1] = dateNow
        setGlobalStorageValue(HIGHSCORE_TEXT_GLOBALSTORAGES[highscoreId], table.concat(text))
        text = {}
    end
end

function getCaughtHighscoreText(highscoreId)
    local ret = getGlobalStorageValue(HIGHSCORE_TEXT_GLOBALSTORAGES[highscoreId])
    if (ret == -1) then
        return "Empty highscore."
    end
    return ret
end