function doUpdateAbilityOutfitCondition(cid)
    if (PokemonAddon.doUpdateAbilityOutfitCondition(cid)) then
        return true
    end

    local outfitCondition = nil
    if (isFlying(cid)) then
        outfitCondition = _G["OUTFIT_FLY_" .. string.upper(string.gsub(getPlayerPokemonName(cid, true), "%s", ""))]

    elseif (isLevitating(cid)) then
        outfitCondition = _G["OUTFIT_LEVITATE_" .. string.upper(string.gsub(getPlayerPokemonName(cid, true), "%s", ""))]

    elseif (isRiding(cid)) then
        outfitCondition = _G["OUTFIT_RIDE_" .. string.upper(string.gsub(getPlayerPokemonName(cid, true), "%s", ""))]

    elseif (isSurfing(cid)) then
        outfitCondition = _G["OUTFIT_SURF_" .. string.upper(string.gsub(getPlayerPokemonName(cid, true), "%s", ""))]

    elseif (isDiving(cid)) then
        outfitCondition = _G["OUTFIT_DIVE_" .. string.upper(string.gsub(getPlayerPokemonName(cid, true), "%s", ""))]
    end

    if (outfitCondition) then
        doAddCondition(cid, outfitCondition)
        return true
    end

    return false
end