local origDoSendMagicEffect = doSendMagicEffect
function doSendMagicEffect(pos, type, player)
    if (ME_POS[type]) then
        pos.x = pos.x + ME_POS[type].x
        pos.y = pos.y + ME_POS[type].y
    end
    origDoSendMagicEffect(pos, type, player)
    if (ME_POS[type]) then -- If we change something on orig pos, who call this will suffer the change also, so lets undo
        pos.x = pos.x - ME_POS[type].x
        pos.y = pos.y - ME_POS[type].y
    end
end

--[[ Function overwrite ]]
local origDoCreateMonster = doCreateMonster
function doCreateMonster(name, pos, displayError, shinyChance)
    if (shinyChance) then
        local info = getMonsterInfo(name, false)
        if (info and info.shiny and getRandom(0, getConfigValue('shinyAppearChance')) == 0) then
            name = info.shiny
        end
    end

    local m = origDoCreateMonster(name, pos, displayError)
    if (isMonster(m)) then
        setMonsterSpecialAbility(m, getPokemonRandomSpecialAbility(getCreatureName(m)))
    end
    return m
end

local _doPlayerPopupFYI = doPlayerPopupFYI
function doPlayerPopupFYI(cid, message)
    if (string.len(message) > FYI_BOX_LENGTH_MAX) then
        local MIN_MARGIN = 50 -- Prevent the same string limit error
        local res = {}
        local str = message
        local antiLoop = 0
        while (string.len(str) > FYI_BOX_LENGTH_MAX and antiLoop < 100) do
            local pos = string.find(str, " ", FYI_BOX_LENGTH_MAX - MIN_MARGIN)
            if (pos) then
                res[#res + 1] = (string.sub(str, 1, pos) .. "(...)")
                str = "(...) " .. string.sub(str, pos + 1, string.len(str))
            else
                break
            end
            antiLoop = antiLoop + 1
        end
        res[#res + 1] = str

        for _, msg in pairs(res) do
            _doPlayerPopupFYI(cid, msg)
        end
        return
    end

    _doPlayerPopupFYI(cid, message)
end
